package Exercicios2;

public class AreaQuadrilatero {

    // area quadrado
    public static int calcularArea (int a){
        return a * a;
    }

    // area retangulo
    public static int calcularArea (int a, int b){
        return a * b;
    }

    // area trapezio
    public static int calcularArea (int a, int b1, int b2){
        return (a / 2) * (b1 * b2);
    }

    
}
